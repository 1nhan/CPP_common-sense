/************************************************************************
*
*	Drill: 사용자 입력 기반 편지 생성 
*	각 단계마다 프로그램을 실행해보며, 정말 기대한 대로 동작하는지 확인하세요. 실수한 부분은 따로 기록해두면, 다음에 같은 실수를 피하는 데 도움이 됩니다.
*	[1] 기본 인사말 출력
*	사용자에게 이름을 입력하도록 요청(prompt)하고 
*	"Hello, first_name"을 출력하는 프로그램을 작성하세요.
*	그 다음, 코드를 수정하여 다음과 같이 변경하세요:
*	프롬프트: "Enter the name of the person you want to write to"
*	출력: "Dear first_name,"
*	쉼표(comma)를 잊지 마세요.
*	
*	[2] 편지 서문 추가
*	다음과 같은 문장을 추가하세요:
*	코드
*	How are you? I am fine. I miss you.
*	첫 줄은 들여쓰기(indent) 하세요.
*	여러분만의 문장을 몇 줄 더 추가해도 좋습니다—이건 여러분의 편지입니다.
*
*	[3] 친구 이름 추가
*	사용자에게 다른 친구의 이름을 입력하도록 요청하고, 이를 friend_name 변수에 저장하세요.
*	편지에 다음 문장을 추가하세요:
*	```코드
*	Have you seen friend_name lately?
*	```
* 
*	[4] 나이 입력 및 조건 처리
*	사용자에게 수신자의 나이(age)를 입력하도록 요청하고, 이를 int 타입 변수 age에 저장하세요.
*	다음 문장을 출력하세요:
*
*	```코드
*	I hear you just had a birthday and you are age years old.
*	```
*	만약 age가 0 이하 또는 110 이상이라면, simple_error("you’re kidding!")를 호출하세요. (이 함수는 PPP_support에서 제공됩니다)
*	
*	[5] 나이에 따른 추가 메시지
*	다음 조건에 따라 문장을 추가하세요:
*	age < 12 → "Next year you will be age+1."
*	age == 17 → "Next year you will be able to vote."
*	age > 70 → "Are you retired?"
*	각 조건에 대해 프로그램이 적절하게 반응하는지 확인하세요.
*
*	[6] 마무리 인사
*	편지의 끝에 다음을 추가하세요:
*
*	```코드
*	Yours sincerely,
*	```
*
*
*	이인한
*	(빈 줄 두 개 후에 본인의 이름을 작성하세요)
*
*************************************************************************/
